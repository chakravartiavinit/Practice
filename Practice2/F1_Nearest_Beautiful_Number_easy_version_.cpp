vector<long long> one;
set<long long> second;
void First()
{
    for (int i = 1; i < 10; ++i)
    {
        long long temp = 0;
        while (temp < INF)
        {
            temp = 10 * temp + i;
            one.push_back(temp);
        }
    }
}
void Second(long long x, int a, int b, int count)
{
    if (x > INF || count > 10)
    {
        return;
    }
    second.insert(x);
    Second(x * (1LL) * 10 + a, a, b, count + 1);
    Second(x * (1LL) * 10 + b, a, b, count + 1);
}
