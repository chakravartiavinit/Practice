vector<pair<long long, long long>> graph[100006];
long long bfs(int source, int dest, int check)
{
    long long answer = 0;
    queue<pair<int, int>> qp;
    qp.push({source, source});
    bool flag = false;
    while (qp.size() > 0)
    {
        pair<int, int> pt;
        pt = qp.front();
        qp.pop();
        for (auto x : graph[pt.first])
        {
            if (x.first > check && x.first != source && x.first != dest)
            {
                return 0;
            }
            if ((x.first != pt.second))
            {
                qp.push({x.first, pt.first});
                answer += x.second;
            }
            if (x.first == dest)
            {
                flag = true;
                return answer;
            }
        }
    }
    if (flag == false)
    {
        return 0;
    }
    return answer;
}
