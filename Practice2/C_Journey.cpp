const int N = int(1e5) + 10;
vector<int> g[N];
double dp[N];
void dfs(int x, int p)
{
    int num = 0;
    dp[x] = 0.0;
    for (auto y : g[x])
    {
        if (y == p)
        {
            continue;
        }
        num++;
        dfs(y, x);
        dp[x] += dp[y] + 1;
    }
    if (num)
    {
        dp[x] /= num;
    }
}
