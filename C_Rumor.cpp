int dfs(int source, int dest, vector<bool> &visited, map<int, vector<pair<int, int>>> &graph)
{
    visited[source] = true;
    queue<pair<int, int>> qp;
    qp.push({source, 0});
    int dist = 0;
    int ans = INT_MAX;
    while (qp.size() > 0)
    {
        pair<int, int> ft;
        ft = qp.front();
        qp.pop();
        for (auto x : graph[ft.first])
        {
            if (visited[x.first] == false)
            {
                visited[x.first] = true;
                dist += x.second;
                qp.push({x.first, x.second});
            }
            else
            {
                if (x.first == dest)
                {
                    debug(dist);
                    ans = min(ans, dist);
                }
                dist = 0;
                break;
            }
            if (x.first == dest)
            {
                debug(dist);
                ans = min(ans, dist);
            }
        }
    }
    return ans;
}
