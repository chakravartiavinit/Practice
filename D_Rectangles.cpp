bool check(pair<long long, long long> p1, pair<long long, long long> p2)
{
    if (abs(p1.first - p2.first) == 0 || (p1.first - p2.second) == 0)
    {
        return true;
    }
    return false;
}
int dfs(pair<long long, long long> p1, int count, int &ans, map<pair<long long, long long>, vector<pair<long long, long long>>> &vt, map<pair<long long, long long>, bool> &visited)
{
    if (count == 4)
    {
        ans++;
        return;
    }
    visited[p1] = true;
    for (auto x : vt[p1])
    {
        if (visited[x] == false)
        {
            dfs(x, count + 1, ans, vt, visited);
        }
    }
}
