vector<int> graph[30005];
int LOG;
void preprocess(int n, vector<int> &parent, vector<int> &depth, vector<vector<int>> &up)
{
    parent[1] = 0;
    for (int i = 1; i <= n; ++i)
    {
        up[i][0] = parent[i];
        for (int j = 1; j < LOG; ++j)
        {
            up[i][j] = up[up[i][j - 1]][j - 1];
        }
    }
}
int query(int node, int k, vector<int> &depth, vector<vector<int>> &up)
{
    if (depth[node] < k)
    {
        return -1;
    }
    for (int j = LOG - 1; j >= 0; j--)
    {
        if (k >= (1 << j))
        {
            node = up[node][j];
            k -= 1 << j;
        }
    }
    return node;
}
void dfs(int source, int pt, vector<int> &parent, vector<int> &depth)
{
    parent[source] = pt;
    depth[source] = depth[pt] + 1;
    for (auto x : graph[source])
    {
        if (x != pt)
        {
            dfs(x, source, parent, depth);
        }
    }
}
