bool dfs(int source, map<int, vector<int>> &graph, int count, vector<bool> &visited)
{
    visited[source] = true;
    for (auto x : graph[source])
    {
        if (visited[x] == false)
        {
            dfs(x, graph, count + 1, visited);
        }
        else
        {
            if (count % 2 == 1)
            {
                return true;
            }
        }
    }
    return false;
}
