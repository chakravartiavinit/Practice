bool compare(pair<string, long long> p1, pair<string, long long> p2)
{
    if (p1.second > p2.second)
    {
        return true;
    }
    else if (p1.second == p2.second)
    {
        if (p1.first.length() > p2.first.length())
        {
            return false;
        }
    }
    return true;
}
